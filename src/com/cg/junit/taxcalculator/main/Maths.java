/*
 * Main Calculation class which implements the four arithmethic operations
 */
package com.cg.junit.taxcalculator.main;

public class Maths {

	public int add(int i, int j) {
		return i+j;
	}

	public int sub(int i, int j) {
		return i-j;
	}

	public int multiply(int i, int j) {
		return i*j;
	}

	public int divide(int i, int j) {
		return i/j;
	}
}
